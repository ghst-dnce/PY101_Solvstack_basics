# number_1 and number_2 are "parameters" to the function
def add_numbers(number_1, number_2):
    print(f"{number_1} + {number_2} = {number_1 + number_2}")

# to "call" the function, type the name of the function and add ().
# the arguments for the data passed in go between ()
add_numbers(500, 750)

# now we can add any two numbers without having to retype the logic
add_numbers(100, 90)
add_numbers(30, 24)